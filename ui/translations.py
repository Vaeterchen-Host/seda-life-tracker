def get_translation(language, key, **kwargs):
    """Return one translated GUI string."""
    text = TRANSLATIONS.get(language, TRANSLATIONS["en"]).get(
        key, TRANSLATIONS["en"].get(key, key)
    )
    return text.format(**kwargs) if kwargs else text
